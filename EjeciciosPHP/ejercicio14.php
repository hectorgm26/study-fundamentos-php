<?php

// Ciclo For
// Partes:
// 1. Inicialización: Se ejecuta una vez al inicio del ciclo. Se utiliza para inicializar una variable de control.
// 2. Condición: Se evalúa antes de cada iteración. Si es verdadera, se ejecuta el bloque de código dentro del ciclo. Si es falsa, se sale del ciclo.
// 3. Incremento: Se ejecuta al final de cada iteración. Se utiliza para modificar la variable de control.

for ($numeroInicial = 0; $numeroInicial <= 10; $numeroInicial++) {
    echo "Numero: " . $numeroInicial . "<br><br>";
}

?>